# Defiindo variavel que será utilizada
nome = "Guilherme Arthur de Carvalho"


# Retornando de acordo com o índice
print(nome[0])

# Retornando do início até um determinado índice
print(nome[:9])

# Retornando de onde deve iniciar
print(nome [10:])

# Retornando especificando o ínicio e o fim
print(nome[10:16])

# Retornando específicando o ínicio, fim e os passos
print(nome[10:16:2])

# Retorando sem parâmetros
print(nome[:])

# Retornando a string ao contrário
print(nome[::-1])