# Declarando duas variáveis
produto_1 = 20
produto_2 = 10

# Somando os valores
print(produto_1 + produto_2)

# Subtraindo os valores
print(produto_1 - produto_2)

# Dividindo os valores
print(produto_1 / produto_2)
print(produto_1 // produto_2)

# Multiplicando os valores
print(produto_1 * produto_2)

# Exponenciação
print(produto_1 ** produto_2)

# Módulo
print(produto_1 % produto_2)